/*
Write a program to check if a given Character is a Vowel or Consonant.

 */

public class Q18 {
    public void vowelconsonants(char c) {
        if (c == 'a' || c == 'e' || c == 'i' || c == 'o' || c == 'u' || c == 'A' || c == 'E' || c == 'I' || c == 'O' || c == 'U') {
            System.out.println("vowels");
        } else {
            System.out.println("consonant");
        }

    }
}
