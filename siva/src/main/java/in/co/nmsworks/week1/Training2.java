public class Training2 {
    String s;

    public void palindrome()
    {
        String s1 = "";
        for(int i=s.length()-1;i>=0;i--)
        {
            s1=s;

        }
        if(s.equals(s1))
            System.out.println("Palindrome");
        else
            System.out.println("Not Palindrome");

    }
}
