package in.co.nmsworks.week1;

public class Rectangle {
    int length;
    int breadth;

    public int area () {
        int area1;
        area1 = length * breadth;
        return area1;
    }
    int perimeter () {
        int peri1;
        peri1 = 2 * (length + breadth);
        return peri1;
    }
}

